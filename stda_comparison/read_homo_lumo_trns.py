homo_lumo_trns = {}

def find_homo_lumo_trns(file_name):
	lines = list(open("%s/%s_excitations.txt" % (file_name, file_name)))

	lines = lines[2:]

	for line in lines:
		values = line.split()
		energy = values[1]
		i = values[6].replace("->", "")
		a = values[7].replace(")", "")

		if int(a) - int(i) == 1:
			row = {"energy" : float(energy),
					"homo" : int(i),
					"lumo" : int(a)
					}

			return row


for i in files:
	homo_lumo_trns[i] = find_homo_lumo_trns(i)

stda_df = pandas.DataFrame.from_dict(homo_lumo_trns).T
